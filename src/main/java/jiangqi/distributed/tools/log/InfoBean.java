package jiangqi.distributed.tools.log;

public class InfoBean {
	private int haomiao;
	private String xiancheng;
	private String leiming;
	private int weizhi;
	private String txt;
	private long threadid;
	
	
	
	public long getThreadid() {
		return threadid;
	}
	public void setThreadid(long threadid) {
		this.threadid = threadid;
	}
	public int getHaomiao() {
		return haomiao;
	}
	public void setHaomiao(int haomiao) {
		this.haomiao = haomiao;
	}
	public String getXiancheng() {
		return xiancheng;
	}
	public void setXiancheng(String xiancheng) {
		this.xiancheng = xiancheng;
	}
	public String getLeiming() {
		return leiming;
	}
	public void setLeiming(String leiming) {
		this.leiming = leiming;
	}
	public int getWeizhi() {
		return weizhi;
	}
	public void setWeizhi(int weizhi) {
		this.weizhi = weizhi;
	}
	public String getTxt() {
		return txt;
	}
	public void setTxt(String txt) {
		this.txt = txt;
	}
	
	
}
